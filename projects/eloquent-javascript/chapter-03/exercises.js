// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  // if n modulo 2 is strictly equal to zero we return 0
if (n % 2 === 0){
  return true;
  // otherwise we just return false
} return false;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  // we need to set a counter variable set to 0
let count = 0;
 // for loop to run through the whole string
for (let i = 0; i < str.length; i++){
  // if the string of [i] is equal to the char we put in, we increase count by one
  if (str[i] === char){
    count++;
  }
  // we return the count OUTSIDE the loop so the whole loop can finish its process
  // if we returned inside it would end hte loop as soon as it hit its first char
} return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
let count = 0;
// same as above, we have a counter, and we simply check each iteration of the string
// to see if it equals 'B', if it does, we increase count by one
for (let i = 0; i < str.length; i++){
  if (str[i] === 'B'){
    count++;
  }
  // we then return the count outside the function
} return count;
}

