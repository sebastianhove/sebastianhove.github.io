// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  // if number 1 is less than number 2 return num1
if (num1 < num2){
  return num1;
  // if the second is less than the first, we return the second
} else if (num2 < num1){
  return num2;
  // default statement if both are even or one isn't a number, we just return 0;
  } return 0;
}

